// Generates 100 gift codes that unlock Ascend Pro for free.
// Writes:
//   GIFT_CODES.txt   — the plain codes to distribute (gitignored, NOT bundled)
//   src/giftcodes.js — only the HASHES ship in the app, for validation
//
// Run: node scripts/gen-gift-codes.js   (regenerates a fresh set of 100)
const fs = require('fs');
const path = require('path');

const ALPHA = 'ABCDEFGHJKMNPQRSTUVWXYZ23456789'; // unambiguous (no I L O 0 1)
function djb2(s) {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) >>> 0;
  return h >>> 0;
}

const codes = new Set();
while (codes.size < 100) {
  let c = 'ASCEND-';
  for (let i = 0; i < 6; i++) c += ALPHA[Math.floor(Math.random() * ALPHA.length)];
  codes.add(c);
}
const list = [...codes];
const hashes = list.map(djb2);

fs.writeFileSync(path.join(__dirname, '..', 'GIFT_CODES.txt'), list.join('\n') + '\n');

const js = `// AUTO-GENERATED by scripts/gen-gift-codes.js — do not edit by hand.
// 100 gift codes that unlock Ascend Pro for free. The plain codes live in
// GIFT_CODES.txt (gitignored, NOT bundled); only their hashes ship in the app.
const HASHES = new Set(${JSON.stringify(hashes)});

function djb2(s) {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) >>> 0;
  return h >>> 0;
}

// True if the entered code is one of the 100 valid gift codes.
export function isValidGiftCode(code) {
  if (!code) return false;
  return HASHES.has(djb2(String(code).trim().toUpperCase()));
}
`;
fs.writeFileSync(path.join(__dirname, '..', 'src', 'giftcodes.js'), js);

console.log('Generated 100 gift codes → GIFT_CODES.txt + src/giftcodes.js');
